#Arithmetic operators - simple calculation

num1 = 45
num2 = 20

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) #Nodulus- Returns remainder

# Comparison operators

X = 78
Y = 60
print(X < Y)
print(X > Y)
print(X <- Y)
print(X >- Y)
print(X -- Y)
print(X != Y)

